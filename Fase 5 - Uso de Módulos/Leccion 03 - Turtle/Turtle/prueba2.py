import turtle as t

t.setup(500,500)

t.shape('turtle')
t.color('purple')
t.bgcolor('black')
t.width(10)

# penup / pendown
t.penup()
t.forward(150)

t.pendown()

t.left(90)

t.forward(100)
t.left(90)
t.forward(300)

t.left(90)
t.forward(200)

t.left(90)
t.forward(300)

t.left(90)
t.forward(100)


t.done()
t.bye()
